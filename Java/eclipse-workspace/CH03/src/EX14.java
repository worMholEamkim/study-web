import java.util.Scanner;
public class EX14 {

	public static void main(String[] args) {
		Scanner rd = new Scanner(System.in);
		int divisor = 0;
		int dividend = 0;
		
		System.out.print("나뉨 수를 입력하시오:");
		dividend = rd.nextInt();
		System.out.println("나눗수를 입력하시오:");
		divisor = rd.nextInt();
		try {
			System.out.print(dividend+"를"+divisor+"로 나누면 몫은"+dividend/divisor+"입니댜.");
		}catch (ArithmeticException e ) {
			System.out.println("0으로 나눌 수 없스빈댜.");
		}
	}
	
}
